document.addEventListener('DOMContentLoaded', () => {
    // Intersection Observer for career items entrance animation
    const observerOptions = {
        threshold: 0.2
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('active');
            }
        });
    }, observerOptions);

    document.querySelectorAll('.career-item').forEach(item => {
        observer.observe(item);
    });

    // Enhanced Physics-based Draggable for nametag with DYNAMIC WIND & SWING
    const container = document.getElementById('nametag-container');
    const card = document.getElementById('nametag-card');
    const lanyardPath = document.getElementById('lanyard-path');
    const lanyardPathShadow = document.getElementById('lanyard-path-shadow');
    
    if (container && card) {
        let isDragging = false;
        let startX, startY;
        let pX = 0, pY = 400; // Previous position for velocity derivation
        let currentX = 0, currentY = 400; // Starting position (hanging down)
        let vX = 0, vY = 0;
        let time = 0;
        
        // Pendulum Settings (BOUNCY & FAST)
        const anchorY = -2000; 
        const stringLen = 2000; 
        const gravity = 1.2; // Much faster fall
        const friction = 0.97; // Smooth damping
        const bounce = 0.15; // Elasticity for "mantul" effect
        
        const updatePhysics = () => {
            const isDark = document.documentElement.classList.contains('dark');
            const isMobile = window.innerWidth < 768;
            time += 0.015;

            if (!isDragging) {
                if (isDark) {
                    // DARK MODE: Zero-G Organic Float
                    const floatX = Math.sin(time * 0.45) * 30 + Math.sin(time * 1.2) * 10;
                    const floatY = Math.cos(time * 0.3) * 20;
                    
                    const ax = (floatX - currentX) * 0.015;
                    const ay = (floatY - currentY) * 0.015;
                    vX += ax; vY += ay;
                    vX *= 0.98; vY *= 0.98;
                    currentX += vX; currentY += vY;
                } else {
                    // LIGHT MODE: BOUNCY PENDULUM
                    vY += gravity; 
                    
                    let nextX = currentX + vX;
                    let nextY = currentY + vY;
                    
                    const dx = nextX;
                    const dy = nextY - anchorY;
                    const dist = Math.sqrt(dx*dx + dy*dy);
                    
                    if (dist > stringLen) {
                        // Spring-back force (Recoil)
                        const overstretch = dist - stringLen;
                        const nx = dx / dist;
                        const ny = dy / dist;
                        
                        // Apply recoil velocity
                        vX -= nx * overstretch * bounce;
                        vY -= ny * overstretch * bounce;
                        
                        // Hard limit to avoid flying away
                        if (dist > stringLen + 50) {
                            nextX = nx * (stringLen + 50);
                            nextY = anchorY + ny * (stringLen + 50);
                        }
                    } 
                    
                    vX *= friction; vY *= friction;
                    vX += Math.sin(time * 1.5) * 0.03;
                    currentX = nextX;
                    currentY = nextY;
                }
            }

            // TILT (Subtle)
            const rotY = (vX * 1.0) + (currentX * 0.02);
            const rotX = (vY * 0.3) - (currentY * 0.02); 
            const rotZ = (vX * 0.2) - (currentX * 0.02);
            
            // X-Offset correction based on alignment
            let offsetX = currentX;
            if (!isMobile) {
                 // Adjust for md:items-start container
                 // On desktop, nametag-container is 40% width, items-start.
                 // The anchor is at container-center.
            }

            card.style.transform = `translate3d(${currentX}px, ${currentY}px, 0) rotateX(${rotX}deg) rotateY(${rotY}deg) rotateZ(${rotZ}deg)`;
            
            // Render Single Stable Lanyard (Stays more vertical)
            if (!isDark && lanyardPath) {
                // Now perfectly matched to the wrapper's 500 center
                const endX = 500 + currentX;
                const endY = 2000 + currentY; 
                const startX_svg = 500;
                const startY_svg = 0;
                
                // Minimize cpX lag to keep it "ga gerak kanan kiri"
                const cpX = 500 + (currentX * 0.1); 
                const cpY = endY * 0.5;
                
                const pathData = `M ${startX_svg} ${startY_svg} Q ${cpX} ${cpY} ${endX} ${endY}`;
                lanyardPath.setAttribute('d', pathData);
                if (lanyardPathShadow) {
                    lanyardPathShadow.setAttribute('d', pathData);
                    lanyardPathShadow.setAttribute('transform', `translate(${vX * 0.5 + 8}, 8)`);
                }
            }
            
            requestAnimationFrame(updatePhysics);
        };
        
        const startDrag = (e) => {
            isDragging = true;
            const pos = e.type.includes('touch') ? e.touches[0] : e;
            startX = pos.clientX - currentX;
            startY = pos.clientY - currentY;
            container.classList.add('grabbing');
        };

        const onDrag = (e) => {
            if (!isDragging) return;
            const pos = e.type.includes('touch') ? e.touches[0] : e;
            currentX = pos.clientX - startX;
            currentY = pos.clientY - startY;
            vX = 0; vY = 0;
        };

        const stopDrag = () => {
            isDragging = false;
            container.classList.remove('grabbing');
        };

        card.addEventListener('mousedown', startDrag);
        window.addEventListener('mousemove', onDrag);
        window.addEventListener('mouseup', stopDrag);

        card.addEventListener('touchstart', startDrag, { passive: false });
        window.addEventListener('touchmove', onDrag, { passive: false });
        window.addEventListener('touchend', stopDrag);
        
        requestAnimationFrame(updatePhysics);
    }
});
